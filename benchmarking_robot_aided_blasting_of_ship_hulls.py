import numpy as np
import skfuzzy as fuzz
from skfuzzy import control as ctrl

p1 = ctrl.Antecedent(np.arange(12, 123, 1), 'p1')
p2 = ctrl.Antecedent(np.arange(23, 234, 1), 'p2')
output = ctrl.Consequent(np.arange(0, 181, 1), 'output')

p1.automf(3)
p2.automf(3)

output['low'] = fuzz.trimf(output.universe, [0, 0, 90])
output['medium'] = fuzz.trimf(output.universe, [0, 90, 180])
output['high'] = fuzz.trimf(output.universe, [90, 180, 180])
rule1 = ctrl.Rule(p1['poor'] | p2['poor'], output['low'])
rule2 = ctrl.Rule(p2['average'], output['medium'])
rule3 = ctrl.Rule(p2['good'] | p1['good'], output['high'])

output_r_ctrl = ctrl.ControlSystem([rule1, rule2, rule3])
output_r = ctrl.ControlSystemSimulation(output_r_ctrl)
output_r.input['p1'] = 102
output_r.input['p2'] = 47

output_r.compute()
print (output_r.output['output'])
output.view(sim=output_r)

