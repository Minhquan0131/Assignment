import numpy as np
import skfuzzy as fuzz
from skfuzzy import control as ctrl

win_speed = ctrl.Antecedent(np.arange(0, 3.8, 0.1), 'win_speed')
temperature = ctrl.Antecedent(np.arange(20.4, 41.2, 0.1), 'temperature')
rain = ctrl.Consequent(np.arange(0.2, 9.1, 0.1), 'rain')

win_speed.automf(3)
temperature.automf(3)

rain['low'] = fuzz.trimf(rain.universe, [0, 0, 4.5])
rain['medium'] = fuzz.trimf(rain.universe, [0, 4.5, 9.0])
rain['high'] = fuzz.trimf(rain.universe, [4.5, 9.0, 9.0])
rule1 = ctrl.Rule(win_speed['poor'] | temperature['poor'], rain['low'])
rule2 = ctrl.Rule(temperature['average'], rain['medium'])
rule3 = ctrl.Rule(temperature['good'] | win_speed['good'], rain['high'])

rainfall_ctrl = ctrl.ControlSystem([rule1, rule2, rule3])
rainfall = ctrl.ControlSystemSimulation(rainfall_ctrl)
rainfall.input['win_speed'] = 1.3
rainfall.input['temperature'] = 36.7

rainfall.compute()
print (rainfall.output['rain'])
rain.view(sim=rainfall)

